import java.util.Scanner;

public class TwoSixteen {
  public static void main(String[] args) {

Scanner input = new Scanner(System.in);

System.out.print("Enter an integer: ");
int x = input.nextInt();

int y = x * x;
int z = 100;

 if(x == z) {
System.out.printf("%d == %d%n", x, z);
}


 if(x >= z) {
System.out.printf("%d >= %d%n", x, z);
}


 if(x <= z) {
System.out.printf("%d <= %d%n", x, z);
}


 if(x != z) {
System.out.printf("%d != %d%n", x, z);
}


 if(x > z) {
System.out.printf("%d > %d%n", x, z);
}


 if(x < z) {
System.out.printf("%d < %d%n", x, z);
}

 if(y == z) {
System.out.printf("%d == %d%n", y, z);
}


 if(y >= z) {
System.out.printf("%d >= %d%n", y, z);
}


 if(y <= z) {
System.out.printf("%d <= %d%n", y, z);
}


 if(y != z) {
System.out.printf("%d != %d%n", y, z);
}


 if(y > z) {
System.out.printf("%d > %d%n", y, z);
}


 if(y < z) {
System.out.printf("%d < %d%n", y, z);
}

}
}

